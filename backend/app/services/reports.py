def _wrap_text(text: str, max_chars: int) -> list[str]:
    words = (text or "").split()
    lines: list[str] = []
    current = ""
    for word in words:
        next_line = f"{current} {word}".strip()
        if len(next_line) > max_chars and current:
            lines.append(current)
            current = word
        else:
            current = next_line
    if current:
        lines.append(current)
    return lines or [""]


def _score_label(total_points: float | None, max_points: float | None) -> str:
    if total_points is not None and max_points is not None:
        return f"{total_points}/{max_points}"
    if total_points is not None:
        return str(total_points)
    return "N/A"


def _merge_config(base: dict[str, Any], override: dict[str, Any] | None) -> dict[str, Any]:
    merged = copy.deepcopy(base)
    if not override:
        return merged

    for key, value in override.items():
        if key == "sections" and isinstance(value, list):
            by_key = {section["key"]: copy.deepcopy(section) for section in merged.get("sections", []) if section.get("key")}
            for raw_section in value:
                if not isinstance(raw_section, dict) or not raw_section.get("key"):
                    continue
                section_key = raw_section["key"]
                current = by_key.get(section_key, {"key": section_key, "label": section_key.replace("_", " ").title(), "enabled": True, "order": 999, "options": {}})
                current.update({k: v for k, v in raw_section.items() if k != "options"})
                current["options"] = {**current.get("options", {}), **raw_section.get("options", {})}
                by_key[section_key] = current
            merged["sections"] = sorted(by_key.values(), key=lambda section: (section.get("order", 999), section.get("label", "")))
        elif isinstance(value, dict) and isinstance(merged.get(key), dict):
            merged[key] = _merge_config(merged[key], value)
        else:
            merged[key] = value
    return merged


def normalize_report_config(config: dict[str, Any] | None) -> dict[str, Any]:
    normalized = _merge_config(BASE_TEMPLATE_CONFIG, config or {})
    normalized["sections"] = sorted(
        normalized.get("sections", []),
        key=lambda section: (int(section.get("order", 999)), str(section.get("label", ""))),
    )
    return normalized


